int	ft_is_prime(int nb)
{
	int	curr;

	curr = 2;
	if (nb <= 1)
		return (0);
	while (curr <= nb)
	{
		if (curr == nb)
			return (1);
		if ((nb % curr) == 0)
			return (0);
		curr++;
	}
	return (0);
}
