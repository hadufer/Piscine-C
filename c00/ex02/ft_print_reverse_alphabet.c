#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	int		i;
	char	c;

	i = 25;
	while (i >= 0)
	{
		c = 'a' + i;
		write(1, &c, 1);
		i--;
	}
	return ;
}
